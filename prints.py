def typewriter(mensage):
    contador = 0
    for caracter in mensage:
      contador = contador + 1
      sys.stdout.write(caracter)
      sys.stdout.flush()
      if caracter != "\n":
        time.sleep(0.03)
      else:
        time.sleep(1)
    return ""

